    private void jButtonPrenotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrenotaActionPerformed
        // TODO add your handling code here:
        String codiceFiscale;
        int numeroCamera, giornoIngresso, giornoUscita, meseIngresso, meseUscita, annoIngresso, annoUscita;
        codiceFiscale = jTextFieldCodiceFiscale.getText();
        numeroCamera = Integer.parseInt(jComboBoxNumeroCamera.getSelectedItem().toString());
        giornoIngresso = Integer.parseInt(jComboBoxGiornoIn.getSelectedItem().toString());
        giornoUscita = Integer.parseInt(jComboBoxGiornoOut.getSelectedItem().toString());
        meseIngresso = Integer.parseInt(jComboBoxMeseIn.getSelectedItem().toString());
        meseUscita = Integer.parseInt(jComboBoxMeseOut.getSelectedItem().toString());
        annoIngresso = Integer.parseInt(jComboBoxAnnoIn.getSelectedItem().toString());
        annoUscita = Integer.parseInt(jComboBoxAnnoOut.getSelectedItem().toString());

        try {
            h.aggiungiPrenotazioneHotel(annoIngresso, meseIngresso, giornoIngresso, annoUscita, meseUscita, giornoUscita, numeroCamera, codiceFiscale);
            JOptionPane.showMessageDialog(null, "Prenotazione Effettuata Con Successo", "Message", JOptionPane.INFORMATION_MESSAGE);
        } catch (DataNonValidaException | DataNonDisponibileException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Errore", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButtonPrenotaActionPerformed

